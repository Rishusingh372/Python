# 7 Data Types

# 1. String -- A string is a sequence of characters enclosed in single quotes (' ') or double quotes (" "). It is used to represent text data. For example:
# name = "John Doe"

# 2. Integer -- An integer is a whole number without a decimal point. It can be positive, negative, or zero. For example:
# age = 30

# 3. Float -- A float is a number that has a decimal point. It can represent both whole numbers and fractional numbers. For example:
# salary = 50000.75

# 4. Boolean -- A boolean is a data type that can have one of two values: True or False. It is used to represent logical values. For example:
# is_student = True

# 5. List -- A list is an ordered collection of items that can be of different data types. It is defined using square brackets [ ]. For example:
# fruits = ["apple", "banana", "cherry"]

# 6. Tuple -- A tuple is similar to a list but is immutable, meaning its elements cannot be changed after it is created. It is defined using parentheses ( ). For example:
# coordinates = (10, 20)

# 7. Set  -- A set is an unordered collection of unique items. It is defined using curly braces { }. For example:
# unique_numbers = {1, 2, 3, 4, 5}

# frozen set -- A frozen set is an immutable version of a set. It is defined using the frozenset() function. For example:
# frozen_numbers = frozenset([1, 2, 3, 4, 5])

# 8. Dictionary -- A dictionary is a collection of key-value pairs. It is defined using curly braces { } with keys and values separated by a colon (:). For example:
# person = {"name": "Alice", "age": 30, "city": "New York"}



